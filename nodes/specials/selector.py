from .. import tree


class VRayNodeSelectObject(bpy.types.Node, tree.VRayObjectNode):
    bl_idname = 'VRayNodeSelectObject'
    bl_label  = 'Object Select'
    bl_icon   = 'VRAY_LOGO'


